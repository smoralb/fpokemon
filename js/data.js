// ---- Fórmulas ----
function expForLevel(l) { return l * l * l; }

function calcStats(spKey, level, ivs) {
  const b = SPECIES[spKey].base;
  const st = (base, iv) => Math.floor(((base + iv) * 2 * level) / 100) + 5;
  return {
    maxhp: Math.floor(((b.hp + ivs.hp) * 2 * level) / 100) + level + 10,
    atk: st(b.atk, ivs.atk), def: st(b.def, ivs.def),
    spe: st(b.spe, ivs.spe), spc: st(b.spc, ivs.spc),
  };
}

function makeMon(spKey, level) {
  const sp = SPECIES[spKey];
  const ivs = { hp: rnd(16), atk: rnd(16), def: rnd(16), spe: rnd(16), spc: rnd(16) };
  const stats = calcStats(spKey, level, ivs);
  const moves = [];
  for (const [lv, mv] of sp.learn) {
    if (lv <= level && !moves.find(m => m.key === mv)) {
      moves.push({ key: mv, pp: MOVES[mv].pp, maxpp: MOVES[mv].pp });
      if (moves.length > 4) moves.shift();
    }
  }
  return {
    id: spKey, name: sp.name, level, exp: expForLevel(level), ivs,
    maxhp: stats.maxhp, hp: stats.maxhp, stats, moves, status: null,
  };
}

function rnd(n) { return Math.floor(Math.random() * n); }

function stageMult(s) {
  s = Math.max(-6, Math.min(6, s));
  return s >= 0 ? (2 + s) / 2 : 2 / (2 - s);
}

// Daño Gen 1 simplificado. stagesA/stagesD: fases activas de cada bando.
function calcDamage(att, def, moveKey, stagesA, stagesD) {
  const mv = MOVES[moveKey];
  const special = SPECIAL_TYPES.includes(mv.type);
  let A = special ? att.stats.spc : att.stats.atk;
  let D = special ? def.stats.spc : def.stats.def;
  A = Math.max(1, Math.floor(A * stageMult(special ? 0 : stagesA.atk)));
  D = Math.max(1, Math.floor(D * stageMult(special ? 0 : stagesD.def)));
  const crit = Math.random() < SPECIES[att.id].base.spe / 512;
  const L = crit ? att.level * 2 : att.level;
  let dmg = Math.floor(Math.floor((2 * L) / 5 + 2) * mv.pow * A / D / 50) + 2;
  if (SPECIES[att.id].types.includes(mv.type)) dmg = Math.floor(dmg * 1.5);
  const eff = typeMult(mv.type, SPECIES[def.id].types);
  dmg = Math.floor(dmg * eff);
  dmg = Math.floor(dmg * (217 + rnd(39)) / 255);
  return { dmg: Math.max(1, dmg), eff, crit };
}

// Probabilidad de captura simplificada (mejor con poca vida o estado alterado).
function catchChance(mon) {
  const sp = SPECIES[mon.id];
  let c = (sp.catch / 255) * (1 - 0.65 * (mon.hp / mon.maxhp));
  if (mon.status) c += 0.12;
  return Math.min(0.95, Math.max(0.05, c));
}

function expGain(defeated) {
  return Math.max(1, Math.floor(SPECIES[defeated.id].exp * defeated.level / 7));
}
