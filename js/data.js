// Convierte las cadenas a matrices de índices (-1 = transparente). Tolerante con anchos.
const SPRITE_PIX = {};
for (const k in SPRITES) {
  const rows = SPRITES[k];
  const m = [];
  for (let y = 0; y < 16; y++) {
    const row = rows[y] || '';
    const line = [];
    for (let x = 0; x < 16; x++) {
      const c = row[x];
      if (c >= '0' && c <= '3') line.push(+c);
      else if (c === ' ' && x > 0) line.push(line[x - 1]); // hueco accidental: copia el anterior
      else line.push(-1);
    }
    m.push(line);
  }
  SPRITE_PIX[k] = m;
}

